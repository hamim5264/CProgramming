//write a program that print integer, float, character and double data type.
#include<stdio.h>
int main()
{
 int num1 = 20;
 float num2 = 5.54;
 double num3 = 10.40656;
 char num4 = 'l';

 printf("integer number is %d\n", num1);
 printf("floating number is %f\n", num2);
 printf("double number is %lf\n", num3);
 printf("character number is %c", num4);
 getch();
}
